n1=int(input("Enter the first number:"))
n2=int(input("Enter the second number:"))

def addition(n1,n2):
    return n1+n2

def sub(n1,n2):
    return n1-n2

def mul(n1,n2):
    return n1*n2

def div(n1,n2):
    return n1/n2

def modules(n1,n2):
    return n1%n2

def expo(n1,n2):
    return n1**n2


print("Addition=",addition(n1,n2))
print("Substraction=",sub(n1,n2))
print("Multiplication=",mul(n1,n2))
print("Division=",div(n1,n2))
print("Modules=",modules(n1,n2))
print("Exponentatiol=",expo(n1,n2))




    